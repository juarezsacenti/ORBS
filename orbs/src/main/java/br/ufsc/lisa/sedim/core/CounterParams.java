package br.ufsc.lisa.sedim.core;

public class CounterParams {

	private String counterType;
	private String userType;
	private String userProperty;
	private String countedProperty;
	private String hierarchyProperty;
	private String directHitProperty;
	private String indirectHitProperty;

	public String getCounterType() {
		return counterType;
	}

	public String getUserType() {
		return userType;
	}

	public String getUserProperty() {
		return userProperty;
	}

	public String getCountedProperty() {
		return countedProperty;
	}

	public String getHierarchyProperty() {
		return hierarchyProperty;
	}

	public String getDirectHitProperty() {
		return directHitProperty;
	}

	public String getIndirectHitProperty() {
		return indirectHitProperty;
	}
}